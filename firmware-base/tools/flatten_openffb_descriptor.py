def format_bytes_block(desc, per_line=12):
    lines = []
    for i in range(0, len(desc), per_line):
        chunk = desc[i:i + per_line]
        lines.append("    " + ", ".join(f"0x{b:02X}" for b in chunk) + ",")
    return "\n".join(lines)


def format_rid_defines():
    width = max(len(name) for name, _, _ in REPORT_IDS)
    lines = []
    for name, value, comment in REPORT_IDS:
        lines.append(f"#define {name:<{width}} 0x{value:02X}  // {comment}")
    return "\n".join(lines)


